    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        try{
          Connection con = conexion.conectar();
         PreparedStatement ps = con.prepareStatement("select * from cursos where codigo = ?"); 
         ps.setString(1, txbuscar.getText().trim());
         
         ResultSet rs = ps.executeQuery();
         
         if(rs.next()){
             txcodigo.setText(rs.getString("codigo"));
             txcurso.setText(rs.getString("curso"));
             txprofesor.setText(rs.getString("profesor"));
             txdia.setText(rs.getString("dia"));
             txhora.setText(rs.getString("hora"));
             txhoras.setText(rs.getString("horas"));
             txcreditos.setText(rs.getString("creditos"));
             txsemestre.setText(rs.getString("semestre"));
         }else{
             label.setText("no disponible");
         }
         }catch(SQLException e){
             System.err.println(e);
             JOptionPane.showMessageDialog(null, "error de software");
         }
    }//GEN-LAST:event_btnbuscarActionPerformed

