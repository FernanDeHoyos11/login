    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
         String curso,profesor,dia,hora;
        String idcurso,idcliente,horas,codigo,semestre,creditos;
        int validacion =0;
        
        codigo = txcodigo.getText().trim();
        curso = txcurso.getText().trim();
        profesor = txprofesor.getText().trim();
        dia = txdia.getText().trim();
        hora = txhora.getText().trim();
        horas = txhoras.getText().trim();
        creditos = txcreditos.getText().trim();
        semestre = txsemestre.getText().trim();
        
        if(codigo.equals("") || curso.equals("") || profesor.equals("") || dia.equals("") ||
           hora.equals("") || horas.equals("") || creditos.equals("") || semestre.equals("")){
            
            txcodigo.setBackground(Color.red);
            txcurso.setBackground(Color.red);
            txprofesor.setBackground(Color.red);
            txdia.setBackground(Color.red);
            txhora.setBackground(Color.red);
            txhoras.setBackground(Color.red);
            txcreditos.setBackground(Color.red);
            txsemestre.setBackground(Color.red);
            
            JOptionPane.showMessageDialog(null, "debe llenar todos los campos");
            
        }else{
           try{
        Connection cn = conexion.conectar();
        PreparedStatement pst = cn.prepareStatement("insert into cursos values(?,?,?,?,?,?,?,?,?,?,?)"); 
        
        pst.setInt(1, 0);
        pst.setInt(2, 0);
        pst.setString(3, codigo);
        pst.setString(4, curso);
        pst.setString(5, profesor);
        pst.setString(6, dia);
        pst.setString(7, hora);
        pst.setString(8, horas);
        pst.setString(9, semestre);
        pst.setString(10, creditos);
        pst.setString(11, "");
        pst.executeUpdate();
        
        cn.close();
        
        txcodigo.setText("");
        txcurso.setText("");
        txprofesor.setText("");
        txdia.setText("");
        txhora.setText("");
        txhoras.setText("");
        txcreditos.setText("");
        txsemestre.setText("");
        
        label.setText("registro exitoso");
        
        }catch(SQLException e){
            System.err.println("error al guardar curso " + e);
             JOptionPane.showMessageDialog(null, "error\n contacte a su proovedor de software");
        }
         
        }
        
        
        
    }//GEN-LAST:event_btnagregarActionPerformed

